class Student:
    def __init__(self,x,y,z):
        self.name=x
        self.roll_No=y
        self.marks=z

    def display(self):
        print(" Student name:{}\n Roll No.:{}\n Marks:{}".format(self.name,self.roll_No,self.marks))


s1=Student("Punamchand",51,81)
s2=Student("Ramesh",10,78)     
s1.display()
s2.display()
    
    